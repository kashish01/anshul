package anshulcode;

public class WhileinWhile {

	public static void main(String[] args) {


			
		
		int num=5;
		
		while(num<=10) {
			
			int table=1;
		while(table<=10) {
			
			System.out.println(num*table +" ");
			table++;
		}
		System.out.println();
		num++;
		}	
			
			
			
			
			
			
			
			
			
			
	}

}
